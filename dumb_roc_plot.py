def plot_roc_curve (x, y):

    sns.set(style='darkgrid')
    plt.title('ROC curve - beginning')
    plt.ylabel('Recall')
    plt.ylim((0.0, 1.0))
    plt.xlabel('Precision')
    plt.xlim((0.0, 1.0))

    for key, x_values in x.iteritems():
        if key == 'not_filtered_beg':
            plt.plot(x_values,y[key], 'b', label = 'not filtered')
        elif key == 'filtered_beg':
            plt.plot(x_values,y[key], 'g', label = 'filtered')
        else:
            plt.plot(x_values,y[key], 'r', label = 'filtered by blacklist')

    plt.legend (loc=2, borderaxespad=0.)
    #plt.legend(handles=[filt, not_filt, blist])
    plt.savefig('figs_ROC_curves/general-beg.png')
    plt.close()



